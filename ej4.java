public class ej4 {
    public static void main(String[] args) {
        int x = 6;
        int y = 2;
        double n = 5.2;
        double m = 2.5;

        // A: El valor de cada variable
        System.out.println("El valor de x es: " + x);
        System.out.println("El valor de y es: " + y);
        System.out.println("El valor de n es: " + n);
        System.out.println("El valor de m es: " + m);

        // B: La suma X + Y
        System.out.println("El resultado de x + y es: " + (x + y));

        // C: La diferencia X - Y
        System.out.println("El resultado de x - y es: " + (x - y));

        // D: El producto X * Y
        System.out.println("El resultado de x * y es: " + (x * y));

        // E: El cociente X / Y
        System.out.println("El resultado de x / y es: " + (x / y));

        // F: El resto X % Y
        System.out.println("El resultado de x % y es: " + (x % y));

        // G: La suma N + M
        System.out.println("El resultado de n + m es: " + (n + m));

        // H: La diferencia N - M
        System.out.println("El resultado de n - m es: " + (n - m));

        // I: El producto N * M
        System.out.println("El resultado de n * m es: " + (n * m));

        // J: El cociente N / M
        System.out.println("El resultado de n / m es: " + (n / m));

        // K: El resto N % M
        System.out.println("El resultado de n % m es: " + (n % m));

        // L: La suma X + N
        System.out.println("El resultado de x + n es: " + (x + n));

        // M: El cociente Y / M
        System.out.println("El resultado de y / m es: " + (y / m));

        // N: El resto Y % M
        System.out.println("El resultado de y % m es: " + (y % m));

        // O: El doble de cada variable
        System.out.println("El doble de x es: " + (x * 2));
        System.out.println("El doble de y es: " + (y * 2));
        System.out.println("El doble de n es: " + (n * 2));
        System.out.println("El doble de m es: " + (m * 2));

        // P: La suma de todas las variables
        System.out.println("La suma de todas las variables es: " + (x + y + n + m));

        // Q: El producto de todas las variables
        System.out.println("El producto de todas las variables es: " + (x * y * n * m));
    }
}
